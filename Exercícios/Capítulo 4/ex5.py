'''Crie um programa que peça ao usuário o tempo inicial de uma viagem (em horas). 
Depois, aumente o tempo da viagem em 3 horas usando o operador de atribuição adequado. Exiba o tempo total de viagem.'''

tempo = int(input("Digite o tempo de viagem: "))

tempo += 3
print("o tempo da viagem com o atraso é de ", tempo)
